
#ruby não é uma linguagem fortemente tipada. 
#semelhante ao javascript pois não precisa especificar o tipo da variável.

#tipo string
produto = 'Macbook Air'

#O método class especifica o tipo da variável.
puts produto.class
puts produto


#float
preco = 5999.99
puts preco.class
puts preco 

#int
quantidade = 10
puts quantidade.class
puts quantidade 

#boolean
disponível = true
puts disponível.class
puts disponível

# uma linguagem com tipagem dinâmica permnite atribui 
# valores diferentes para a mesma variável
quantidade = '9'
puts quantidade.class
puts quantidade 
